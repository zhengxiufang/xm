function show(canshu) {
    var info = originData.info
    for (var idx in info) {
        var p = document.createElement("p")
        p.innerText = info[idx].name
        document.querySelector(".ai-nav").appendChild(p)
        p.index_ai = idx
        test(canshu, info)
        p.onmouseenter = function(e) {
            test(e.target.index_ai, info)
        }
    }
}

